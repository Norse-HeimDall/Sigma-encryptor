/**
 * @file main.cpp
 * @brief Главная точка входа приложения Sigma Encryptor
 * 
 * Этот файл содержит функцию main(), которая инициализирует приложение,
 * настраивает графический интерфейс и запускает главный цикл обработки событий.
 * 
 * Приложение использует:
 * - GLFW для создания окна и обработки ввода
 * - OpenGL 3 для рендеринга графического интерфейса
 * - ImGui для создания пользовательского интерфейса в стиле Obsidian
 * - OpenSSL для криптографических операций
 * 
 * @author Sigma Team
 * @version 1.0
 */

#include <iostream>     // Стандартный ввод/вывод
#include <memory>       // Умные указатели
#include <stdexcept>    // Исключения стандартной библиотеки
#include <clocale>      // Для setlocale
#include <windows.h>    // Для SetConsoleCP

// Заголовочные файлы проекта
#include "application.hpp"  // Главный класс приложения

/**
 * @brief Настраивает UTF-8 кодировку для консоли Windows
 */
void setupConsoleEncoding()
{
    // Устанавливаем локаль для поддержки UTF-8
    std::setlocale(LC_ALL, ".UTF8");
    
    // Настраиваем кодовую страницу консоли на UTF-8 (65001)
    SetConsoleCP(65001);
    SetConsoleOutputCP(65001);
    
    // Устанавливаем режим вывода UTF-8 для std::cout
    std::cout.sync_with_stdio(false);
    
    // Также выводим тест русских символов
    std::cout << "Console encoding: UTF-8" << std::endl;
}

/**
 * @brief Главная функция приложения
 * 
 * Создаёт экземпляр приложения, инициализирует его и запускает главный цикл.
 * В случае ошибки выводит сообщение в консоль и возвращает код ошибки.
 * 
 * @return 0 при успешном завершении, 1 при ошибке
 */
int main()
{
    // Настраиваем UTF-8 кодировку консоли
    setupConsoleEncoding();
    
    try
    {
        // Создаём умный указатель на приложение
        // Используем unique_ptr для автоматического управления памятью
        std::unique_ptr<sigma::Application> app = std::make_unique<sigma::Application>();
        
        // Инициализируем приложение (создаём окно, настраиваем OpenGL и ImGui)
        if (!app->initialize())
        {
            // Выводим сообщение об ошибке в stderr
            std::cerr << "[ОШИБКА] Не удалось инициализировать приложение" << std::endl;
            return 1;   // Возвращаем код ошибки
        }
        
        // Запускаем главный цикл приложения
        // Это бесконечный цикл, который обрабатывает события и рендерит интерфейс
        // до тех пор, пока пользователь не закроет окно
        app->run();
        
        // Выводим сообщение о корректном завершении
        std::cout << "[ИНФО] Приложение успешно завершено" << std::endl;
        
        // Возвращаем 0 - успешное завершение без ошибок
        return 0;
    }
    catch (const std::exception& e)
    {
        // Перехватываем все стандартные исключения
        // Это позволяет корректно обработать критические ошибки
        std::cerr << "[КРИТИЧЕСКАЯ ОШИБКА] " << e.what() << std::endl;
        return 1;   // Возвращаем код ошибки
    }
    catch (...)
    {
        // Перехватываем все остальные исключения
        // Это "ловушка" для неизвестных ошибок
        std::cerr << "[КРИТИЧЕСКАЯ ОШИБКА] Неизвестная ошибка" << std::endl;
        return 1;   // Возвращаем код ошибки
    }
}

