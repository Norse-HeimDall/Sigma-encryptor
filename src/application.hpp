/**
 * @file application.hpp
 * @brief Заголовочный файл главного класса приложения Sigma Encryptor
 * 
 * Этот файл содержит объявление класса Application, который управляет
 * всем жизненным циклом приложения: инициализацией, главным циклом,
 * обработкой событий и очисткой ресурсов.
 * 
 * Класс инкапсулирует:
 * - Окно GLFW и контекст OpenGL
 * - Контекст ImGui для рендеринга UI
 * - Логику пользовательского интерфейса
 * 
 * @author Sigma Team
 * @version 1.0
 */

#ifndef SIGMA_APPLICATION_HPP
#define SIGMA_APPLICATION_HPP

// Стандартные библиотеки C++
#include <string>       // Строки std::string
#include <memory>       // Умные указатели
#include <vector>       // Векторы

// GLFW - библиотека для создания окон и обработки ввода
// GLFW автоматически включит заголовочные файлы OpenGL
#include <GLFW/glfw3.h>

// Предварительные объявления классов
namespace sigma
{

// Класс рендерера (управляет OpenGL и ImGui)
class Renderer;

// Класс UI (управляет пользовательским интерфейсом)
class UI;

// Перечисление возможных состояний приложения
enum class AppState
{
    Running,    // Приложение активно работает
    ShuttingDown,  // Приложение завершается
    Error       // Произошла ошибка
};

/**
 * @class Application
 * @brief Главный класс приложения Sigma Encryptor
 * 
 * Этот класс управляет всем приложением. Он создаёт окно,
 * инициализирует графические библиотеки и запускает главный цикл.
 * 
 * Использует паттерн "RAII" (Resource Acquisition Is Initialization)
 * для автоматического управления ресурсами.
 */
class Application
{
public:
    /**
     * @brief Конструктор по умолчанию
     * 
     * Инициализирует все внутренние компоненты в нулевые состояния.
     * Реальная инициализация происходит в методе initialize().
     */
    Application();

    /**
     * @brief Деструктор
     * 
     * Освобождает все выделенные ресурсы: удаляет окно,
     * завершает работу библиотек GLFW и ImGui.
     */
    ~Application();

    /**
     * @brief Инициализирует приложение
     * 
     * Создаёт окно, настраивает OpenGL, инициализирует ImGui.
     * Это метод, который нужно вызвать после конструктора.
     * 
     * @return true если инициализация прошла успешно
     * @return false если произошла ошибка
     */
    bool initialize();

    /**
     * @brief Запускает главный цикл приложения
     * 
     * Это бесконечный цикл, который:
     * 1. Обрабатывает события ввода (клавиатура, мышь)
     * 2. Обновляет логику приложения
     * 3. Рендерит графический интерфейс
     * 
     * Цикл продолжается пока окно не будет закрыто.
     */
    void run();

    /**
     * @brief Останавливает приложение
     * 
     * Устанавливает состояние в ShuttingDown, что приводит
     * к выходу из главного цикла после завершения текущей итерации.
     */
    void shutdown();

private:
    /**
     * @brief Обрабатывает входящие события
     * 
     * Вызывает GLFW для обработки событий окна,
     * клавиатуры и мыши.
     */
    void processEvents();

    /**
     * @brief Обновляет состояние приложения
     * 
     * Вызывается каждый кадр для обновления
     * логики приложения (анимации, таймеры и т.д.).
     */
    void update();

    /**
     * @brief Рендерит кадр
     * 
     * Очищает экран, рендерит ImGui,
     * и выполняет Swap Buffers для отображения.
     */
    void render();

    /**
     * @brief Освобождает все ресурсы
     * 
     * Вызывается при завершении работы для
     * корректного освобождения памяти и библиотек.
     */
    void cleanup();

    // Умные указатели на компоненты приложения
    std::unique_ptr<Renderer> m_renderer;  // Рендерер (OpenGL + ImGui)
    std::unique_ptr<UI> m_ui;               // Пользовательский интерфейс

    // Указатель на окно GLFW
    // GLFWwindow - это непрозрачный тип, представляющий окно
    GLFWwindow* m_window = nullptr;

    // Текущее состояние приложения
    AppState m_state = AppState::Running;

    // Заголовок окна
    std::string m_title = "Sigma Encryptor";

    // Ширина и высота окна в пикселях
    int m_width = 900;
    int m_height = 600;

    // Флаг, указывающий, что приложение должно закрыться
    bool m_shouldClose = false;
};

} // namespace sigma

#endif // SIGMA_APPLICATION_HPP

